const strandByTitle = title => {
  if (
    [
      'Atomic structure',
      'Atoms, molecules & stoichiometry',
      'Chemical bonding',
      'States of matter',
      'Chemical energetics',
      'Electrochemistry',
      'Equilibria',
      'Reaction kinetics',
    ].includes(title)
  ) return 'Physical chemistry'

  if (
    [
      'The Periodic Table: chemical periodicity',
      'Group 2',
      'Group 17',
      'Nitrogen and sulfur',
      'Chemistry of the transition elements',
    ].includes(title)
  ) return 'Inorganic chemistry'

  if (title === 'Analytical techniques') return 'Analysis'

  return 'Organic chemistry'
}

function topic({
  id,
  syllabusNumber,
  level,
  pathway,
  course,
  title,
  teachingHours,
  teachingTimeLabel,
  teachingOrder,
  teachingOrderLabel,
  vocabulary = [],
  progress = 0,
}) {
  const timeLabel = teachingTimeLabel || `${teachingHours} hours`
  const orderLabel = teachingOrderLabel || `Teaching order ${teachingOrder}`

  return {
    id,
    syllabusNumber,
    level,
    pathway,
    course,
    unit: strandByTitle(title),
    title,
    difficulty: 'Cambridge 9701',
    teachingHours,
    teachingTimeLabel: timeLabel,
    teachingOrder,
    teachingOrderLabel: orderLabel,
    lessons: typeof teachingHours === 'number' ? Math.max(1, Math.ceil(teachingHours / 2)) : 1,
    progress,
    description: `Cambridge International ${level} Chemistry 9701 topic ${syllabusNumber}: ${title}. Recommended teaching time: ${timeLabel}; ${orderLabel.toLowerCase()}.`,
    vocabulary,
  }
}

